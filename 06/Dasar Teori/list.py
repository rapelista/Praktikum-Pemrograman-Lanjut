# Creating a List
List = []
print("Blank List: ")
print(List)

# List of numbers
List = [10, 20, 14]
print("\nList of numbers: ")
print(List)

# using index
List = ["Red", "Green", "yellow", 10, 11]
print("\nList Items: ")
print(List[0])
print(List[2])
print(List[4])

# Multi-Dimensional List
List = [['Red', 'Green'], ['Yellow']]
print("\nMulti-Dimensional List: ")
print(List)
